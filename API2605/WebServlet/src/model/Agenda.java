package model;

import java.text.SimpleDateFormat;

public class Agenda {
	private int idCompromisso;
	private String cnpjOrigem;
	private String cnpjDest;
	private String data;
	private String horario;
	private String descricao;
	private String titulo;
	private boolean agendado;
	
	public int getIdCompromisso() {
		return idCompromisso;
	}
	public void setIdCompromisso(int idCompromisso) {
		this.idCompromisso = idCompromisso;
	}
	public String getCnpjOrigem() {
		return cnpjOrigem;
	}
	public void setCnpjOrigem(String cnpjOrigem) {
		this.cnpjOrigem = cnpjOrigem;
	}
	public String getCnpjDest() {
		return cnpjDest;
	}
	public void setCnpjDest(String cnpjDest) {
		this.cnpjDest = cnpjDest;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getHorario() {
		return horario;
	}
	public void setHorario(String horario) {
		this.horario = horario;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public boolean getAgendado() {
		return agendado;
	}
	public void setAgendado(boolean agendado) {
		this.agendado = agendado;
	}

	public String parseUtilDate(String d) {
		return new SimpleDateFormat("dd/MM/yyyy").format(d);
	}
}
